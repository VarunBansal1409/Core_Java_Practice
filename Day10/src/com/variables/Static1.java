package com.variables;

public class Static1 {
	
	static byte b;
	static short s;
	static int i;
	static long l;
	static boolean bl;
	static char c;
	static float f;
	static double d;
	static String str;
	
	public static void main(String[] args) {
		
		System.out.println(Static1.b);
		System.out.println(Static1.s);
		System.out.println(Static1.i);
		System.out.println(Static1.l);
		System.out.println(Static1.bl);
		System.out.println(Static1.c);
		System.out.println(Static1.f);
		System.out.println(Static1.d);
		System.out.println(Static1.str);
		
	}
}
